    

class Configuration:
    def __init__(self, input, output, layers, iterations, learning_rate):
        self.input = input
        self.output = output
        self.layers = layers
        self.iterations = iterations 
        self.learning_rate = learning_rate

    def input():
        return self.input

    def output():
        return self.output

    def layers():
        return self.layers

    def iterations():
        return self.iterations

    def learning_rate():
        return self.learning_rate
